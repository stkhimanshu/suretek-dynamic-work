    <script>
      // Bootstrap modal instance

      const modalEl = document.getElementById('applyModal');

      const modal = new bootstrap.Modal(modalEl);



      // Elements

      const flipCard = document.getElementById('formWrapper');

      const form = document.getElementById('jobApplyForm');

      const formMessage = document.getElementById('formMessage');

      const formSubMessage = document.getElementById('formSubMessage');

      const closeModalBtn = document.getElementById('closeModalBtn');



      // Open modal for specific job

      document.querySelectorAll('.job-card a').forEach(btn => {

        btn.addEventListener('click', function(e) {

          e.preventDefault();

          const jobTitle = this.closest('.job-card').querySelector('h3').innerText;

          document.getElementById('jobPosition').value = jobTitle;



          // Reset form and flip state

          form.reset();

          flipCard.classList.remove('flipped');



          modal.show();

        });

      });



      // Handle form submission via AJAX

      form.addEventListener('submit', function(e) {

        e.preventDefault();

        const formData = new FormData(form);



        fetch('apply.php', {

            method: 'POST',

            body: formData

          })

          .then(res => res.json())

          .then(data => {

            if (data.status === 'success') {

              formMessage.textContent = "🎉 Application Submitted!";

              formMessage.classList.remove('text-danger');

              formMessage.classList.add('text-success');

              formSubMessage.textContent = data.message;

            } else {

              formMessage.textContent = "❌ Submission Failed!";

              formMessage.classList.remove('text-success');

              formMessage.classList.add('text-danger');

              formSubMessage.textContent = data.message;

            }



            // Flip the card to show success/error

            flipCard.classList.add('flipped');


          })

          .catch(err => {

            formMessage.textContent = "❌ Submission Failed!";

            formMessage.classList.remove('text-success');

            formMessage.classList.add('text-danger');

            formSubMessage.textContent = "Network error. Please try again.";

            flipCard.classList.add('flipped');

            console.error(err);

          });

      });



      // Close modal from back side

      closeModalBtn.addEventListener('click', function() {

        modal.hide();

      });
    </script>



