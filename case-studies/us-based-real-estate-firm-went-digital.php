                    <!-- Client Profile -->

                    <div class="client_intro">

                        <h3 id="client-profile">Client Profile</h3>
                        <p>Our client is a reputed real estate appraisal firm based in the United States, specializing in property and land valuation. Known for accuracy and trust, the firm aimed to modernize its traditional, paper-based process. They wanted to move towards a <a href="<?= $baseurl ?>mobile-app-development.php" title="Mobile First Workflow - Suretek Infosoft">mobile-first workflow</a> that allowed field appraisers to collect, store, and analyze property data instantly.</p>

                    </div>


                    <!-- Business Challenge -->

                    <h3 id="business-challenge">Business Challenge</h3>

                    <p>The client's appraisal process was manual and time-consuming, relying on handwritten notes and paper forms. Inspectors had to carry files, take photos separately, and later upload or document everything at the office. This not only delayed reporting but also increased the risk of data loss and inconsistencies. The client needed a single mobile platform where appraisers could capture property details, images, and sketches in real time, even when working offline.
                    </p>
                    <!-- Solution -->

                    <h3 id="solution">Suretek's Solution</h3>
                    <p>Suretek developed a custom Android application that transformed the way property assessments were conducted. The app enabled appraisers to record property details, capture high-quality photos and videos, and draw layouts using an intuitive digital canvas.
                    </p>

                    <h5>Key features included:</h5>
                    <ul class="list">
                        <li><b>Property Management:</b> Add, edit, and organize property listings with address details and inspection scheduling.
                        </li>
                        <li><b>Interactive Drawing Canvas:</b> A built-in sketching tool to draw architectural layouts using pencils, erasers, and stroke tools.
                        </li>
                        <li><b>Property Detailing:</b> Smart digital forms to enter room counts, measurements, and other structural details.
                        </li>
                        <li><b>Photo & Video Capture:</b> Integrated camera functionality for visual documentation directly linked to each property record.
                        </li>
                        <li><b>Centralized Storage:</b> All data securely synced to a cloud-based database, accessible anytime, anywhere.
                        </li>
                    </ul>
                    <p>The result was a smooth, paperless, and efficient workflow that brought mobility and accuracy to daily operations.
                    </p>


                    <h3 id="impact">Impact Delivered</h3>
                    <p>The mobile solution drastically improved field productivity and data accuracy. Appraisers could now complete inspections faster, capture details instantly, and share insights with the office team in real time. The firm experienced:</p>

                    <ul class="list">
                        <li>50% faster property evaluations through digital data entry.</li>
                        <li>Improved collaboration between on-site appraisers and office staff.</li>
                        <li>Zero data loss, with all records stored securely in the cloud.</li>
                        <li>Higher client satisfaction due to faster and more accurate reports.</li>
                    </ul>

                    <p>The app became a reliable daily tool, replacing paperwork with efficiency and precision.</p>


                    <h3 id="tech-stack">Technologies Used</h3>

                    <ul class="list">
                        <li><b>Frontend (Mobile):</b> Kotlin, Jetpack Compose, Android SDK 14+</li>
                        <li><b>Backend:</b> Node.js, Express.js, PHP 8.2 (for legacy integration)</li>
                        <li><b>Database:</b> MySQL 8.0, Firebase Realtime Database</li>
                        <li><b>APIs:</b> RESTful APIs, JSON</li>
                        <li><b>Cloud & Storage:</b> AWS EC2, S3</li>
                        <li><b>Security:</b> OAuth 2.0, HTTPS encryption</li>
                        <li><b>DevOps:</b> GitHub Actions (CI/CD automation)</li>
                    </ul>



