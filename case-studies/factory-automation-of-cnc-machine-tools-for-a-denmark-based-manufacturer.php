                    <!-- Client Profile -->

                    <div class="client_intro">

                        <h3 id="client-profile">Client Profile</h3>

                        <p>A leading manufacturing company based in Denmark, the client specializes in precision machining and mechanical fabrication. They operate a highly automated production line for components made of metal (steel, aluminum, brass), plastics, rubber, electronic parts, and batteries, all under one roof.</p>

                        <p>Renowned for their commitment to quality and innovation, the client builds customized mechanical components based on intricate CAD drawings and complex assembly guidelines. Their operations involve high-volume, high-mix production across sectors like automotive, electronics, and industrial machinery.</p>

                    </div>



                    <!-- Business Challenge -->

                    <h3 id="business-challenge">Business Challenge</h3>

                    <p>In an era where speed, precision, and uptime define success, the client was still dependent on legacy CNC systems requiring <strong>manual intervention and on-site configuration</strong>. Every time a new machining order came in, factory managers had to <strong>physically operate the CNC panel</strong> to set parameters — a time-consuming and error-prone task.</p>

                    <p>This dependency on physical presence caused:</p>

                    <ul class="list">

                        <li><strong>Reduced flexibility in operations</strong></li>

                        <li><strong>Delays in production cycles</strong></li>

                        <li><strong>Risk of human error in manual configuration</strong></li>

                        <li><strong>Lack of centralized oversight or remote monitoring</strong></li>

                    </ul>

                    <p>They needed a <strong>remote, intelligent solution</strong> that would free machine operations from the factory floor, enabling <strong>real-time control, monitoring, and reconfiguration</strong> of CNC parameters securely, globally, and 24/7.</p>

                    <!-- Solution -->

                    <h3 id="solution">Suretek’s Smart Solution</h3>

                    <p>To transform the client’s CNC operations, Suretek Infosoft engineered a <strong>cutting-edge Remote CNC Automation System</strong> powered by modern web technologies, industrial IoT, and secure cloud architecture.</p>

                    <p>Here’s what we built and delivered:</p>

                    <h4>Web-Based CNC Access Platform</h4>

                    <p>A <strong>cross-platform, responsive web application</strong> that allows authorized users to remotely:</p>

                    <ul class="list">

                        <li>Configure CNC parameters</li>

                        <li>Monitor operational status</li>

                        <li>Access machining data</li>

                        <li>Perform real-time edits on machining instructions</li>

                    </ul>

                    <h4>Role-Based Access & Security</h4>

                    <p>Implemented <strong>granular, role-based access control (RBAC)</strong> with secure login layers, ensuring that only verified personnel can alter machining parameters or access sensitive configuration files.</p>

                    <h4>Industrial IoT & Edge Connectivity</h4>

                    <p>Connected CNC machines via <strong>Ethernet & MQTT-based edge gateways</strong>, enabling secure machine-to-cloud communication and <strong>bidirectional data sync</strong> — without impacting existing control panel setups.</p>

                    <h4>Real-Time Monitoring & Alerts</h4>

                    <p>Integrated <strong>AI-powered analytics and monitoring dashboards</strong> built with <strong>React.js and Node.js</strong>, capable of:</p>

                    <ul class="list">

                        <li>Showing live CNC statuses</li>

                        <li>Triggering alerts on anomaly detection</li>

                        <li>Generating log reports with time-stamped changes</li>

                    </ul>

                    <h4>Cloud Backup & Recovery</h4>

                    <p>Every parameter change is auto-backed-up to a secure <strong>Azure Blob Storage</strong>, with full <strong>disaster recovery support</strong> — ensuring no data loss in case of control failure or power disruption.</p>

                    <h3 id="tech-stack">Key Technologies Used</h3>

                    <ul class="list">

                        <li><strong>Frontend</strong>: React.js, HTML5, CSS3</li>

                        <li><strong>Backend</strong>: Node.js, .NET Core, ASP.NET MVC</li>

                        <li><strong>Database</strong>: SQL Server 2019, MongoDB (for log management)</li>

                        <li><strong>IoT Communication</strong>: MQTT, OPC UA</li>

                        <li><strong>Cloud</strong>: Microsoft Azure – Blob Storage, App Services</li>

                        <li><strong>Security</strong>: OAuth 2.0, Role-Based Access Control (RBAC)</li>

                        <li><strong>Other Tools</strong>: Grafana for visual monitoring, Git for version control</li>

                    </ul>                    

                    <h3 id="impact">Impact Delivered</h3>

                    <p><strong>Remote Accessibility 24/7</strong> <br/> Factory personnel can now control CNC machines from anywhere — reducing downtime, improving flexibility, and enabling true <strong>"lights-out" manufacturing</strong>.</p>

                    <p><strong>40% Faster Setup Time</strong> <br/> Eliminated the need for physical intervention, resulting in <strong>faster batch configuration</strong> and setup transitions across shifts.</p>

                    <p><strong>Reduced Human Errors by 60%</strong> <br/> The structured UI and alert-based safety nets ensured parameter input is consistent, validated, and error-free.</p>

                    <p><strong>Cost Savings on Labor & Travel</strong> <br/> The client reported significant savings by <strong>cutting on-site operator hours</strong> and eliminating emergency factory visits.</p>

                    <p><strong>Scalable Solution</strong> <br/> The system supports integration with multiple CNC machines, and is already being scaled across other production lines.</p>

                    <h3 id="matters">Why This Project Matters</h3>

                    <p>In today’s manufacturing landscape, <strong>automation alone isn’t enough</strong>. Factories need <strong>intelligence, connectivity, and flexibility</strong> — especially when managing high-variety, low-volume production.</p>

                    <p>By enabling remote control of CNC machines, this solution:</p>

                    <ul class="list">

                        <li><strong>Decentralizes machining control</strong></li>

                        <li><strong>Drives operational efficiency</strong></li>

                        <li><strong>Enhances responsiveness to order changes</strong></li>

                        <li><strong>Future-proofs factories for Industry 4.0</strong></li>

                    </ul>

                    <p>It’s not just about automating machines — it’s about <strong>empowering people</strong> to manage them smarter.</p>

