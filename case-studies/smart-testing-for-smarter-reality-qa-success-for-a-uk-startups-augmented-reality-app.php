                    <!-- Client Profile -->

                    <div class="client_intro">

                        <h3 id="client-profile">Client Profile</h3>

                        <p>A UK-based technology startup set out to launch an <strong>Augmented Reality (AR) mobile application</strong> designed to deliver immersive experiences through real-time interaction with virtual 3D objects using mobile screens and AR glasses. </p>

                    </div>


                    <p>The application is designed for use across various industries, from engineering and architecture to gaming and education. It offers users the ability to interact with digital structures, creatures, and data models in real-world environments.</p>



                    <!-- Business Challenge -->

                    <h3 id="business-challenge">Business Challenge</h3>

                    <p>The client needed a dedicated QA partner to validate the performance, accuracy, and user experience of their <a href="<?= $baseurl ?>mobile-app-development.php">AR mobile application</a>. With growing user expectations in the UK’s tech-savvy market, the client sought to ensure:
                    </p>

                    <ul class="list">

                        <li>Real-time, lag-free object tracking
                        </li>

                        <li>Stability across varying device capabilities
                        </li>

                        <li>Seamless performance under changing lighting and movement conditions</li>

                        <li>High usability and accessibility for a diverse user base</li>



                    </ul>

                    <p>The application also used visual markers (like QR codes and image targets) to render 3D models and required precision in marker recognition across devices, camera types, and environments. This called for deep testing expertise in Augmented Reality technology.
                    </p>

                    <!-- Solution -->

                    <h3 id="solution">Suretek’s Solution</h3>

                    <p>At Suretek Infosoft, we deployed a team of AR testing specialists with experience in immersive technology QA, mobile automation, and Unity-based testing. Our goal was to simulate real-world conditions remotely and validate both functional and non-functional aspects of the app to ensure readiness for public release.
                    </p>

                    <p>We leveraged the latest tools and methodologies for cross-platform AR testing, incorporating AI-powered test automation, computer vision-based recognition testing, and remote device cloud testing to streamline the process.
                    </p>

                    <h3 id="testing">Testing Scope and Scenarios</h3>

                    <div class="mb-3"></div>

                    <h5>Real-Time Object Tracking Validation
                    </h5>

                    <p>
                        Tested how accurately the app detects and aligns 3D objects over AR markers across various phone models and screen resolutions.

                    </p>
                    <h5>Multi-Device Compatibility
                    </h5>

                    <p>Assessed app behavior on high-end vs. low-end Android and iOS devices—focusing on camera quality, CPU load, and memory consumption.
                    </p>

                    <h5>Environmental Simulation Testing
                    </h5>

                    <p>Verified app performance under different lighting, angles, obstructions, ambient noise, and user motion using AR testing labs and simulated environments.
                    </p>

                    <h5>Interactive UI Element Testing
                    </h5>

                    <p>Ensured all interactive 3D elements (like clickable buttons on models) correctly redirected to external links or information layers.
                    </p>
                    <h5>2D/3D Object Recognition Testing
                    </h5>

                    <p>Validated object mapping precision to ensure <a href="https://medium.com/@Suretek_infosoft">AR content</a> was correctly rendered based on visual markers.
                    </p>

                    <h5>Accessibility Testing
                    </h5>

                    <p>Assessed UI/UX for inclusivity across age groups and physical abilities, ensuring AR navigation was smooth and intuitive.</p>

                    <h5>Security & Data Handling
                    </h5>

                    <p>Ensured no sensitive user data was compromised while interacting with external content or when permissions (like camera/mic) were accessed.
                    </p>

                    <h3 id="impact">Impact Delivered</h3>

                    <ul class="list">

                        <li>Identified and resolved UI inconsistencies through GUI testing
                        </li>

                        <li>Detected logic flaws and system crashes via functional and exploratory testing
                        </li>

                        <li>Ensured data protection through rigorous security assessments
                        </li>

                        <li>Simulated real-world scenarios remotely using device farms and automation
                        </li>

                        <li>Enhanced app responsiveness and stability across the UK’s popular mobile devices
                        </li>

                    </ul>



                    <h3 id="tech-stack">Technologies Used</h3>


                    <ul class="list">

                        <li><strong>AR Testing Frameworks</strong>: Unity AR Foundation, Apple ARKit, Google ARCore
                        </li>

                        <li><strong>Automation Tools</strong>: Appium, Selenium, BrowserStack Live, TestProject
                        </li>

                        <li><strong>Performance Tools</strong>: Firebase Performance Monitoring, Charles Proxy, JMeter
                        </li>

                        <li><strong>Bug Reporting & Tracking</strong>: Jira, TestRail, Allure Reports
                        </li>
                        <li><strong>Device Farms</strong>: AWS Device Farm, LambdaTest for remote cross-device testing

                        </li>
                        <li><strong>Accessibility Tools</strong>: AXE DevTools, TalkBack, VoiceOver

                        </li>

                    </ul>


