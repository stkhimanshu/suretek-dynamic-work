                    <!-- Client Profile -->

                    <div class="client_intro">

                        <h3 id="client-profile">Client Profile</h3>

                        <p>Our client is a leading cultural travel operator in Russia, renowned for crafting immersive tours that seamlessly blend history, cuisine, and local experiences. Operating since the early 2000s, they have built a reputation for authentic journeys but realized that today’s travelers expect more than guidebooks or generic mobile apps. To stay competitive, they aimed to embrace digital transformation and deliver real-time, hyper-personalized travel experiences.</p>

                    </div>



                    <!-- Business Challenge -->

                    <h3 id="business-challenge">Business Challenge</h3>

                    <p>Traditional tourism apps often lack <strong>real-time personalization and contextual insights</strong>, leaving tourists with generic recommendations. With global travel platforms entering the market, the client needed to stand out by:</p>

                    <ul class="list">

                        <li>Delivering <strong>AI-driven, location-based recommendations</strong></li>

                        <li>Sending <strong>real-time push alerts</strong> with curated offers</li>

                        <li>Unlocking new revenue streams via <strong>advertiser partnerships</strong></li>

                        <li>Ensuring <strong>seamless cross-platform accessibility (Web, iOS & Android)</strong></li>

                    </ul>

                    <p>The vision: a modern smart travel assistant that dynamically engages tourists while enabling local businesses to connect with them at the right place and time.</p>

                    <!-- Solution -->

                    <h3 id="solution">Suretek’s Solution</h3>

                    <p>We developed an AI-powered, geo-intelligent tour guide platform—a digital companion that transforms exploration into a personalized journey.</p>

                    <h4>Key Highlights</h4>

                    <ul class="list">

                        <li><strong>Location Intelligence 2.0</strong> – Integrated <strong>Mapbox + Google Maps Platform</strong> with advanced <strong>geofencing</strong> to push hyper-localized recommendations when users are near museums, restaurants, or cultural landmarks.</li>

                        <li><strong>AI Personalization Engine</strong> – Leveraged <strong>machine learning (TensorFlow Lite + AWS Personalize)</strong> to recommend tours, restaurants, and experiences based on user preferences, travel history, and time of day.</li>

                        <li><strong>Multi-User Ecosystem</strong> – Role-based interfaces for Tourists, Admins, and Advertisers with unique dashboards.</li>

                        <li><strong>Dynamic CMS</strong> – AI-assisted content management system to update tours, promotions, and campaigns in real-time.</li>

                        <li><strong>Revenue Model</strong> – Advertisers can create campaigns, upload creatives, manage budgets, and track performance with <strong>real-time analytics.</strong></li>

                    </ul>

                    <p>The platform adapts to each traveler’s journey, making exploration spontaneous, relevant, and rewarding.</p>

                    <h3 class="work">Suretek's Contribution</h3>

                    <p>Our team worked closely with the client through the full product lifecycle—from market research and UX design to development and deployment. We Delivered:</p>

                    <h4>For Tourists</h4>

                    <ul class="list">

                        <li>Cross-platform app (iOS, Android, Web) built with <strong>React Native + Next.js</strong></li>

                        <li>AI-driven recommendations with <strong>real-time location tracking</strong></li>

                        <li>Smart push notifications for offers & events</li>

                        <li><strong>Multilingual & voice-assisted navigation</strong> powered by <strong>Azure Cognitive Services</strong></li>

                        <li>Interactive AR overlays for landmarks (using <strong>Azure Cognitive Services</strong>)</li>



                    </ul>

                    <h4>For Admins</h4>

                    <ul class="list">

                        <li>Cloud-native CMS with <strong>Headless architecture</strong> (Strapi + GraphQL)</li>

                        <li><strong>Kibana-powered dashboards</strong> for insights & engagement metrics</li>

                        <li>Granular geo-targeting controls for campaign management</li>

                    </ul>

                    <h4>For Advertisers</h4>

                    <ul class="list">

                        <li>Self-service portal to upload creatives, manage campaigns, and view <strong>real-time ROI metrics</strong></li>

                        <li>Integrated <strong>Stripe & Razorpay gateways</strong> for seamless billing</li>

                        <li>Lead capture forms connected to <strong>CRM integrations (HubSpot, Salesforce)</strong></li>

                    </ul>

                    <p>We ensured a <strong>responsive, intuitive UI/UX</strong> optimized for mobile-first travelers while providing a <strong>web mirror experience</strong> for global accessibility.</p>

                    <h4>Impact Delivered</h4>

                    <ul class="list">

                        <li><strong>+42% increase in user engagement</strong> with AI-driven recommendations & geo-fenced alerts</li>

                        <li><strong>Higher advertiser ROI</strong> through precision targeting & real-time insights</li>

                        <li><strong>New revenue streams unlocked</strong> via in-app promotions & subscription packages</li>

                        <li><strong>Tourist satisfaction boosted</strong>, enabling effortless discovery of hidden gems</li>

                        <li><strong>Scalable architecture</strong> ready for global expansion in new markets</li>

                    </ul>

                    <p>The client evolved from a traditional tour operator into a <strong>digital-first, experience-driven travel brand</strong>.</p>

                    <h3 id="tech-stack">Technologies Used</h3>

                    <ul class="list">

                        <li><strong>Frontend</strong>: React Native (Mobile), Next.js (Web)</li>

                        <li><strong>Backend</strong>: Node.js with NestJS (microservices architecture)</li>

                        <li><strong>Database</strong>: PostgreSQL + Redis (for caching)</li>

                        <li><strong>Maps & Geo</strong>: Google Maps Platform, Mapbox, Geofencing SDK</li>

                        <li><strong>AI & ML</strong>: TensorFlow Lite, AWS Personalize, OpenAI Embeddings (for recommendations & personalization)</li>

                        <li><strong>Authentication</strong>: Auth0 with OAuth 2.1 + biometric login</li>

                        <li><strong>Notifications</strong>: Firebase Cloud Messaging + OneSignal</li>

                        <li><strong>Payments</strong>: Stripe, Razorpay</li>

                        <li><strong>Analytics</strong>: Mixpanel, Google Analytics 4, OpenTelemetry</li>

                        <li><strong>APIs</strong>: GraphQL + RESTful APIs</li>

                        <li><strong>Cloud & DevOps</strong>: AWS (EKS, Lambda, S3, CloudWatch, DynamoDB for logs), Terraform, Docker, Kubernetes</li>

                        <li><strong>AR/VR</strong>: ARCore & ARKit for immersive exploration</li>

                    </ul>

