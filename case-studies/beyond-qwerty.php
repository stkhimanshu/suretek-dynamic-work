                        <li><b>Machine Learning: </b>TensorFlow Lite for auto-correct and suggestions
                        </li>
                        <li><b>Voice Recognition:</b> Google Speech-to-Text API
                        </li>
                        <li><b>Database:</b> Firebase Realtime Database
