                    <ul class="list">
                        <li><b>Frontend:</b> React.js
                        </li>
                        <li><b>Backend:</b> Node.js with Express
                        </li>
                        <li><b>Database:</b> PostgreSQL</li>
                        <li><b>Cloud Platform:</b> AWS
