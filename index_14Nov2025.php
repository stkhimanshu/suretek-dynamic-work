<script>
    // Initialize Bootstrap tooltips
    document.addEventListener("DOMContentLoaded", function() {
        var tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'));
        tooltipTriggerList.map(function(tooltipTriggerEl) {
            return new bootstrap.Tooltip(tooltipTriggerEl);
        });
    });

    const toggleBtn = document.getElementById("toggleBtn");
    const content = document.querySelector(".collapse-content");
    const collapsedHeight = 420; // collapsed height

    toggleBtn.addEventListener("click", function(e) {
        e.preventDefault();

        const icon = this.querySelector("i");
        const tooltip = bootstrap.Tooltip.getInstance(this); // get tooltip instance

        // ✅ hide tooltip immediately on click
        tooltip.hide();

        if (content.classList.contains("expanded")) {
            // Collapse
            content.style.maxHeight = collapsedHeight + "px";
            content.classList.remove("expanded");

            // Change to down arrow
            icon.classList.remove("fa-chevrons-up");
            icon.classList.add("fa-chevrons-down");

            // Update tooltip text
            this.setAttribute("title", "View More");
            tooltip.setContent({
                '.tooltip-inner': 'View More'
            });

            // ✅ scroll to top of logo section after collapsing
            document.querySelector("#logo_bar").scrollIntoView({
                behavior: "smooth"
            });

        } else {
            // Expand
            const fullHeight = Array.from(content.children).reduce((acc, child) => {
                return acc + child.offsetHeight + parseInt(getComputedStyle(child).marginBottom);
            }, 0);

            content.style.maxHeight = fullHeight + "px";
            content.classList.add("expanded");

            // Change to up arrow
            icon.classList.remove("fa-chevrons-down");
            icon.classList.add("fa-chevrons-up");

            // Update tooltip text
            this.setAttribute("title", "View Less");
            tooltip.setContent({
                '.tooltip-inner': 'View Less'
            });

            // ✅ scroll down to show the logo section when expanding
            document.querySelector("#logo_bar").scrollIntoView({
                behavior: "smooth"
            });
        }
    });
</script>


